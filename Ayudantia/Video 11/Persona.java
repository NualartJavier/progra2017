package ayudantia.tema11;

/**
 *
 * @author juan
 */
public class Persona {
    private int edad;
    private int id;
    private String Nombre;

    public Persona(int edad, int id, String Nombre) {
        this.edad = edad;
        this.id = id;
        this.Nombre = Nombre;
    }

    public int getEdad() {
        return edad;
    }

    public void setEdad(int edad) {
        this.edad = edad;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNombre() {
        return Nombre;
    }

    public void setNombre(String Nombre) {
        this.Nombre = Nombre;
    }
    
}
